export const baseCategories = rawCategories.map((category) => ({
  ...category,
  words: category.words.map((word) => String(word).trim()).filter(Boolean),
}));

export function buildWordBank(base = baseCategories, customWords = []) {
  const list = [];
  base.forEach((category) => {
    category.words.forEach((word, index) => {
      list.push({
        id: `${category.key}-${index}`,
        en: word,
        emoji: category.emoji,
        color: category.color,
        categoryKey: category.key,
        categoryLabel: category.labelEn,
        categoryLabelZh: category.labelZh,
        categoryEmoji: category.emoji,
        isCustom: false,
      });
    });
  });

  customWords.forEach((word) => {
    const category = baseCategories.find((item) => item.key === word.categoryKey);
    list.unshift({
      id: word.id || `custom-${word.en}-${word.categoryKey}`,
      en: word.en,
      emoji: word.emoji || category?.emoji || "✨",
      color: word.color || category?.color || "#ff7b54",
      categoryKey: word.categoryKey,
      categoryLabel: word.categoryLabel || category?.labelEn || "Custom",
      categoryLabelZh: word.categoryLabelZh || category?.labelZh || "自定义",
      categoryEmoji: word.categoryEmoji || category?.emoji || "✨",
      isCustom: true,
    });
  });

  return list;
}

export function countWords() {
  return baseCategories.reduce((sum, category) => sum + category.words.length, 0);
}
